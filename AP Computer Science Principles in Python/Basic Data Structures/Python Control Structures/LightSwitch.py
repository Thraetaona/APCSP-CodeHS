# This program introduces the 'not'
# operator, which lets you take the
# opposite of a boolean value. Here
# we simulate a light switch.


light_on = True
print("Light on? " + str(light_on))

light_on = not light_on
print("Light on? " + str(light_on))

light_on = not light_on	
print("Light on? " + str(light_on))